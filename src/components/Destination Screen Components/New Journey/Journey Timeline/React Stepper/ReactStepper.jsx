import React, { useState } from "react";
import "./ReactStepper.css";
import Box from "@mui/material/Box";
import Stepper from "@mui/material/Stepper";
import Step from "@mui/material/Step";
import StepButton from "@mui/material/StepButton";
import Route from "../Timeline Sections/Route/Route";
import Transport from "../Timeline Sections/Transport/Transport";
import Accomodation from "../Timeline Sections/Accomodation/Accomodation";
import Overview from "../Timeline Sections/Overview/Overview";

const steps = ["Route", "Transport", "Accomodation", "Overview"];

function ReactStepper() {
  const [activeStep, setActiveStep] = useState(0);
  const [completed, setCompleted] = useState({});

  const totalSteps = () => {
    return steps.length;
  };

  const completedSteps = () => {
    return Object.keys(completed).length;
  };

  const isLastStep = () => {
    return activeStep === totalSteps() - 1;
  };

  const allStepsCompleted = () => {
    return completedSteps() === totalSteps();
  };

  const handleNext = () => {
    const newActiveStep =
      isLastStep() && !allStepsCompleted()
        ? // It's the last step, but not all steps have been completed,
          // find the first step that has been completed
          steps.findIndex((step, i) => !(i in completed))
        : activeStep + 1;
    setActiveStep(newActiveStep);
  };

  const handleBack = () => {
    setActiveStep((prevActiveStep) => prevActiveStep - 1);
  };

  const handleStep = (step) => () => {
    setActiveStep(step);
    console.log("this is Step", step);
    if (step === 0) {
      return (
        <div>
          <h3>
            this is <h2>Div 0</h2>{" "}
          </h3>
        </div>
      );
    } else if (step === "1") {
      <div>
        <h3>
          this is <h2>Div 1</h2>{" "}
        </h3>
      </div>;
    }
  };

  const handleComplete = () => {
    const newCompleted = completed;
    newCompleted[activeStep] = true;
    setCompleted(newCompleted);
    handleNext();
  };

  const handleReset = () => {
    setActiveStep(0);
    setCompleted({});
  };
  return (
    <Box sx={{ width: "100%" }}>
      <Stepper nonLinear activeStep={activeStep}>
        {steps.map((label, index) => (
          <Step key={label} completed={completed[index]}>
            <StepButton color="inherit" onClick={handleStep(index)}>
              {label}
            </StepButton>
          </Step>
        ))}
      </Stepper>
      <div>
        {(activeStep === 0 && <Route />) ||
          (activeStep === 1 && <Transport />) ||
          (activeStep === 2 && <Accomodation />) ||
          (activeStep === 3 && <Overview />)}
      </div>
    </Box>
  );
}

export default ReactStepper;

